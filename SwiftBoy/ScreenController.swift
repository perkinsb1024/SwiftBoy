import Foundation
import Cocoa

let WIDTH = 160
let HEIGHT = 144

public struct PixelData {
    var a:UInt8 = 255
    var r:UInt8
    var g:UInt8
    var b:UInt8
}

class ScreenController {
    let screen: ScreenView
    let memoryManager: MemoryManager
    let width = WIDTH
    let height = HEIGHT
    var dataBuffer = [Int](repeating: 0, count: (WIDTH * HEIGHT))
    var pixelBuffer = [PixelData](repeating: PixelData(a: 255, r: 255, g: 255, b: 255), count: (WIDTH * HEIGHT))
    let rgbColorSpace = CGColorSpaceCreateDeviceRGB()
    let bitmapInfo:CGBitmapInfo = CGBitmapInfo(rawValue: CGImageAlphaInfo.premultipliedFirst.rawValue)
    
    var pixelSize: Int = 2
    var gbColor1: NSColor = NSColor.lightGray
    var gbColor2: NSColor = NSColor.gray
    var gbColor3: NSColor = NSColor.darkGray
    var gbColor4: NSColor = NSColor.black
    
    let gbPixelColor1 = PixelData(a: 255, r: 232, g: 232, b:232)
    let gbPixelColor2 = PixelData(a: 255, r: 164, g: 164, b:164)
    let gbPixelColor3 = PixelData(a: 255, r: 096, g: 096, b:096)
    let gbPixelColor4 = PixelData(a: 255, r: 000, g: 000, b:000)
    
    init(screen: ScreenView, memoryManager: MemoryManager) {
        self.screen = screen
        self.memoryManager = memoryManager
    }
    
    func randomize() {
        for row in 0 ..< height {
            for col in 0 ..< width {
                switch(Int(arc4random_uniform(4))) {
                case 0:
                    pixelBuffer[row * width + col] = gbPixelColor1
                case 1:
                    pixelBuffer[row * width + col] = gbPixelColor2
                case 2:
                    pixelBuffer[row * width + col] = gbPixelColor3
                default:
                    pixelBuffer[row * width + col] = gbPixelColor4
                }
            }
        }
    }
    
    private func colorFromCode(_ colorCode : Int) -> PixelData? {
        switch(colorCode) {
        case 0:
            return gbPixelColor1
        case 1:
            return gbPixelColor2
        case 2:
            return gbPixelColor3
        case 3:
            return gbPixelColor4
        case _:
            return nil
        }
    }
    
    func set(x: Int, y: Int, toColor colorCode : Int) {
        guard x >= 0 && x < width && y >= 0 && y < height else {
            return
        }
        
        guard let color = colorFromCode(colorCode) else {
            return
        }
        guard width * y + x < pixelBuffer.count else {
            return
        }
        pixelBuffer[width * y + x] = color
    }
    
    func fill(colorCode : Int) {
        guard let color = colorFromCode(colorCode) else {
            return
        }
        pixelBuffer = [PixelData](repeating: color, count: (width * height))
    }
    
    func clear() {
        fill(colorCode: 0)
    }
    
    func update() {
        screen.image = imageFromARGB32Bitmap(pixels: pixelBuffer, width: width, height: height)
        screen.update()
    }
}

// Draw Screen Functions
extension ScreenController {
    func drawScreen(sX: UInt8) {
        let lcdc = UInt8(0xDB)//memoryManager.readMemory(offset: 0xFF40, length: 1)
        clear()
        // Check if screen is enabled
        guard lcdc & UInt8(0x80) != 0 else {
           return
        }
        drawBackground(lcdc: lcdc, sX: sX)
        drawWindow(lcdc: lcdc)
        drawObjects(lcdc: lcdc)
        update()
    }
    
    func drawBackground(lcdc: UInt8, sX: UInt8) {
        // Check that BG is enabled
        guard lcdc & UInt8(0x1) != 0 else {
            return
        }
        //let sX = UInt8(0);
        let sY = UInt8(0);
        /*
        let mapDataLocation = lcdc & UInt8(0x3) == 0 ? 0x9800 : 0x9C00
        // Todo: if using second bgData, index by unsigned int
        let bgDataLocation = lcdc & UInt8(0x4) == 0 ? 0x8000 : 0x8800
        let mapData = memoryManager.readMemory(offset: mapDataLocation, length: 1024)
        let bgData = memoryManager.readMemory(offset: bgDataLocation, length: 1024)
        */
        var mapData = [UInt8](repeating: 0, count: 1024)
        var bgData = fontData
        
        for i in 0..<20 {
            mapData[i * 32 + i ..< i * 32 + i + 15] = [0x48,0x65,0x6C,0x6C,0x6F,0x2C,0x20,0x57,0x6F,0x72,0x6C,0x64,0x21]
        }
 
        for col in Int(sX) ..< Int(sX+20) {
            for row in Int(sY) ..< Int(sY+18) {
                let memCol = col % 32
                let memRow = row % 32
                let dispCol = col - Int(sX)
                let dispRow = row - Int(sY)
                let tileNum = mapData[Int(memRow * 32 + memCol)]
                let tileData = Array(bgData[Int(tileNum) * 16 ..< Int(tileNum) * 16 + 16])
                for byte in 0 ..< 8 {
                    for bit in 0 ..< 8 {
                        let color: Int = (tileData[byte] & UInt8(0x1 << (7 - bit)) == 0 ? 0 : 2) +
                            (tileData[byte + 8] & UInt8(0x1 << (7 - bit)) == 0 ? 0 : 1)
                        set(x: dispCol * 8 + bit, y: dispRow * 8 + byte, toColor: color)
                    }
                }
            }
        }
    }
    
    func drawWindow(lcdc: UInt8) {
        // Check that Window is enabled
        guard lcdc & UInt8(0x1) != 0 else {
            return
        }
    }
    
    func drawObjects(lcdc: UInt8) {
        // Check that Objects are enabled
        guard lcdc & UInt8(0x2) != 0 else {
            return
        }
        
    }
}

// Image Conversion Functions

extension ScreenController {
    func imageFromARGB32Bitmap(pixels:[PixelData], width:Int, height:Int)->NSImage {
        let bitsPerComponent = 8
        let bitsPerPixel = 32
        
        assert(pixels.count == Int(width * height))
        
        var data = pixels
        let providerRef = CGDataProvider(
            data: NSData(bytes: &data, length: data.count * MemoryLayout<PixelData>.size)
        )
        
        let cgim = CGImage(
            width: width,
            height: height,
            bitsPerComponent: bitsPerComponent,
            bitsPerPixel: bitsPerPixel,
            bytesPerRow: width * MemoryLayout<PixelData>.size,
            space: rgbColorSpace,
            bitmapInfo: bitmapInfo,
            provider: providerRef!,
            decode: nil,
            shouldInterpolate: true,
            intent: .defaultIntent
        )
        return NSImage.init(cgImage: cgim!, size: NSSize.init(width: width, height: height))
    }
}

